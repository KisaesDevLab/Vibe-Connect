function startsWithPdfMagic(bytes: Uint8Array): boolean {
  // %PDF- in ASCII: 0x25 0x50 0x44 0x46 0x2d
  return (
    bytes[0] === 0x25 &&
    bytes[1] === 0x50 &&
    bytes[2] === 0x44 &&
    bytes[3] === 0x46 &&
    bytes[4] === 0x2d
  );
}

describe('isPdfConvertible', () => {
  it('accepts every image format the wrapper knows how to convert', () => {
    expect(isPdfConvertible('image/jpeg')).toBe(true);
    expect(isPdfConvertible('image/jpg')).toBe(true);
    expect(isPdfConvertible('image/png')).toBe(true);
    expect(isPdfConvertible('image/webp')).toBe(true);
    expect(isPdfConvertible('image/gif')).toBe(true);
    expect(isPdfConvertible('image/heic')).toBe(true);
    expect(isPdfConvertible('image/heif')).toBe(true);
    // Case-insensitive — UAs sometimes capitalize.
    expect(isPdfConvertible('IMAGE/JPEG')).toBe(true);
  });

  it('rejects non-image and unsupported image types', () => {
    expect(isPdfConvertible('application/pdf')).toBe(false);
    expect(isPdfConvertible('text/plain')).toBe(false);
    expect(isPdfConvertible('image/svg+xml')).toBe(false);
    expect(isPdfConvertible('image/bmp')).toBe(false);
    expect(isPdfConvertible('')).toBe(false);
  });
});

async function pageCount(pdf: Uint8Array): Promise<number> {
  // Round-trip through pdf-lib's parser so the page count assertion
  // doesn't depend on any particular writer layout (indirect refs,
  // compressed object streams, etc.).
  const { PDFDocument } = await import('pdf-lib');
  const parsed = await PDFDocument.load(pdf);
  return parsed.getPageCount();
}

describe('imagesToPdf', () => {
  it('wraps a PNG in a one-page PDF', async () => {
    const out = await imagesToPdf([{ bytes: TINY_PNG, mimeType: 'image/png' }]);
    expect(out).toBeInstanceOf(Uint8Array);
    expect(startsWithPdfMagic(out)).toBe(true);
    expect(await pageCount(out)).toBe(1);
  });

  it('wraps a JPEG in a one-page PDF', async () => {
    const out = await imagesToPdf([{ bytes: TINY_JPEG, mimeType: 'image/jpeg' }]);
    expect(startsWithPdfMagic(out)).toBe(true);
    expect(await pageCount(out)).toBe(1);
  });

  it('combines two images into a two-page PDF', async () => {
    const out = await imagesToPdf([
      { bytes: TINY_PNG, mimeType: 'image/png' },
      { bytes: TINY_JPEG, mimeType: 'image/jpeg' },
    ]);
    expect(startsWithPdfMagic(out)).toBe(true);
    expect(await pageCount(out)).toBe(2);
  });

  it('throws on an empty input', async () => {
    await expect(imagesToPdf([])).rejects.toThrow(/no images/);
  });
});
